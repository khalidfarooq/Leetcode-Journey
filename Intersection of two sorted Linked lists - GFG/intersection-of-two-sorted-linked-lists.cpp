Node* findIntersection(Node* head1, Node* head2)
{
    Node *l1 = head1, *l2 = head2;
    Node *dummy = new Node(0);
    Node *head = dummy;
    
    while(l1 && l2){
        if(l1->data != l2->data){
            if(l1->data < l2->data) l1 = l1->next;
            else l2 = l2->next;
        }
        else{
            Node *newnode = new Node(l1->data);
            head->next = newnode;
            head = head->next;
            l1 = l1->next;
            l2 = l2->next;
        }
    }
   return dummy->next;
    
}